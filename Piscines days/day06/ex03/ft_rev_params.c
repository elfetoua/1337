void	ft_putchar(char c);

int		main(int argc, char **argv)
{
	int		i;
	int		j;

	argc--;
	i = argc;
	while (i > 0)
	{
		j = 0;
		while (argv[i][j] != '\0')
		{
			ft_putchar(argv[i][j]);
			j++;
		}
		ft_putchar('\n');
		i--;
	}
	return (0);
}
