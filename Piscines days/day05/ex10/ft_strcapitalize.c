char	*ft_strcapitalize(char *str)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[0] >= 'a' && str[0] <= 'z')
			str[0] -= 32;
		if ((str[i] >= 'a' && str[i] <= 'z') && (str[i - 1] >= 0
					&& str[i - 1] <= 47))
			str[i] -= 32;
		if ((str[i] >= 'A' && str[i] <= 'Z') && (str[i - 1] >= 0
					&& str[i - 1] <= 47))
			str[i] = str[i];
		else if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] += 32;
		i++;
	}
	return (str);
}
